//===========================================================================
/*!	@brief		押下チェック
	@param		button	ボタン番号
	@return		----
*/
//===========================================================================
bool MouseEventGetter::IsOn( u32 button ) const
{
	
	return m_event.IsOn( button );
}

//===========================================================================
/*!	@brief		座標取得
	@param[out]	x,y
	@return		----
*/
//===========================================================================
void MouseEventGetter::GetPos( u32* x, u32* y ) const
{
	
	if( x ) {
		*x	= m_event.x;
	}
	if( y ) {
		*y	= m_event.y;
	}
}

