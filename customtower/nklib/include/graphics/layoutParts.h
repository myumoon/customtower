#ifndef __LAYOUT_PARTS_H__
#define __LAYOUT_PARTS_H__

#include <vector>
#include "../math/vector.h"


namespace nk {
namespace graphics {


//=============================================================================
/*!
								レイアウトパーツ
*/
//=============================================================================
class LayoutParts {
public:
	//-----------------------型定義--------------------------
	

	//----------------------静的メンバ-----------------------
	
	
	//-----------------------メソッド------------------------

	//===========================================================================
	/*!	@brief		更新
	*/
	//===========================================================================
	virtual void	UpdateParts( void )	{}

	//===========================================================================
	/*!	@brief		描画
	*/
	//===========================================================================
	virtual void	DrawParts( void ) {}

	//===========================================================================
	/*!	@brief		パーツ名取得
	*/
	//===========================================================================
	const char*		GetName( void ) const;

	//===========================================================================
	/*!	@brief		パーツ名設定
	*/
	//===========================================================================
	void			SetName( const char* name );

	//===========================================================================
	/*!	@brief		パーツ名が一緒ならtrueを返す
	*/
	//===========================================================================
	bool			IsSameName( const char* name ) const;

	//===========================================================================
	/*!	@brief		タグ名取得
	*/
	//===========================================================================
	const char*		GetTagName( void ) const;

	//===========================================================================
	/*!	@brief		アクティブ設定
	*/
	//===========================================================================
	void			SetActive( bool active );

	//===========================================================================
	/*!	@brief		アクティブならtrueを返す
	*/
	//===========================================================================
	bool			IsActive( void ) const;

	//===========================================================================
	/*!	@brief		表示設定
	*/
	//===========================================================================
	void			SetVisible( bool visible );

	//===========================================================================
	/*!	@brief		表示ならtrueを返す
	*/
	//===========================================================================
	bool			IsVisible( void ) const;

	//===========================================================================
	/*!	@brief		位置設定
	*/
	//===========================================================================
	virtual void	SetPos( const math::Vector& pos );
	virtual void	SetPos( s32 x, s32 y );

	//===========================================================================
	/*!	@brief		位置取得
	*/
	//===========================================================================
	math::Vector	GetPos( void ) const;

	//===========================================================================
	/*!	@brief		サイズセット
	*/
	//===========================================================================
	void			SetSize( const math::Vector& size );
	void			SetSize( s32 w, s32 h );

	//===========================================================================
	/*!	@brief		サイズ取得
	*/
	//===========================================================================
	math::Vector	GetSize( void ) const;

	
	
protected:
	//===========================================================================
	/*!	@brief		タグ名設定
	*/
	//===========================================================================
	void			SetTagName( const char* tagname );

protected:
	//----------------------メンバ変数-----------------------
	char			m_tagName[256];		//!< タグ名
	char			m_name[256];		//!< パーツ名
	bool			m_active;			//!< アクティブ状態
	bool			m_visible;			//!< 表示状態
	math::Vector	m_pos;				//!< 表示位置
	math::Vector	m_size;				//!< サイズ
	
private:
	//-----------------------メソッド------------------------
	void	_Init( void );
	void	_Term( void );


public:
	//-------------コンストラクタ・デストラクタ--------------
			LayoutParts();
	virtual~LayoutParts();
};


	
}	// namespace graphics	
}	// namespace nk



#endif  // __LAYOUT_PARTS_H__
