//===========================================================================
/*!	@brief	コンストラクタ
	@param	----
*/
//===========================================================================
BattleInfo::BattleInfo()
{
	_Init();
}


//===========================================================================
/*!	@brief	デストラクタ
	@param	----
*/
//===========================================================================
BattleInfo::~BattleInfo()
{
	_Term();
}


//===========================================================================
/*!	@brief	初期化
	@param	----
	@return	----
*/
//===========================================================================
void BattleInfo::_Init( void )
{
	stageType			= STAGE_TYPE_UNKNOWN;
	contorollerNum		= 0;
	for( s32 i = 0; i < MAX_CONTROLLER_NUM; ++i ) {
		contorollerTypes[i]	= CONTOROLLER_TYPE_NONE;
	}
	
}


//===========================================================================
/*!	@brief	終了処理
	@param	----
	@return	----
*/
//===========================================================================
void BattleInfo::_Term( void )
{

}


//===========================================================================
/*!	@brief		ステージ設定
	@param		type
	@return		----
*/
//===========================================================================
void BattleInfo::SetStage( STAGE_TYPE type )
{
	stageType	= type;
}



//===========================================================================
/*!	@brief		コントローラー設定
	@param		type
	@return		----
*/
//===========================================================================
void BattleInfo::AddControler( CONTOROLLER_TYPE type )
{
	contorollerTypes[contorollerNum]	= type;
	++contorollerNum;
}



